CREATE TABLE individu (num_ind INT PRIMARY KEY, nom VARCHAR(90), prenom VARCHAR(90));
CREATE TABLE cinema (num_cine INT PRIMARY KEY, nom VARCHAR(90), adresse VARCHAR(90));
CREATE TABLE film (num_film INT PRIMARY KEY, num_ind INT REFERENCES individu(num_ind), titre VARCHAR(90), genre VARCHAR(90), annee INT);
CREATE TABLE projection (num_cine INT REFERENCES cinema(num_cine), num_film VARCHAR(90) REFERENCES film(num_film), dates DATE, PRIMARY KEY(num_cine,num_film,dates));
CREATE TABLE jouer (num_ind INT REFERENCES individu(num_ind), num_film INT REFERENCES film(num_film), rôle VARCHAR(90), PRIMARY KEY(num_ind,num_film));

